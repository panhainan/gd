package com.gd.entity;

import java.util.Date;

public class Au {
	private long au_id;
	private Activity au_acti;
	private User au_user;
	private double au_eval_score;
	private String au_eval_content;
	private Date au_eval_time;
	private boolean au_eval_support;
	private boolean au_join; // 是否报名参加
	private boolean au_arrive;// 是否签到
	private boolean au_read;
	private boolean au_comment;

	public Au() {
		super();
	}

	public long getAu_id() {
		return au_id;
	}

	public void setAu_id(long au_id) {
		this.au_id = au_id;
	}

	public Activity getAu_acti() {
		return au_acti;
	}

	public void setAu_acti(Activity au_acti) {
		this.au_acti = au_acti;
	}

	public User getAu_user() {
		return au_user;
	}

	public void setAu_user(User au_user) {
		this.au_user = au_user;
	}

	public double getAu_eval_score() {
		return au_eval_score;
	}

	public void setAu_eval_score(double au_eval_score) {
		this.au_eval_score = au_eval_score;
	}

	public String getAu_eval_content() {
		return au_eval_content;
	}

	public void setAu_eval_content(String au_eval_content) {
		this.au_eval_content = au_eval_content;
	}

	public Date getAu_eval_time() {
		return au_eval_time;
	}

	public void setAu_eval_time(Date au_eval_time) {
		this.au_eval_time = au_eval_time;
	}

	public boolean isAu_eval_support() {
		return au_eval_support;
	}

	public void setAu_eval_support(boolean au_eval_support) {
		this.au_eval_support = au_eval_support;
	}

	public boolean isAu_join() {
		return au_join;
	}

	public void setAu_join(boolean au_join) {
		this.au_join = au_join;
	}

	public boolean isAu_arrive() {
		return au_arrive;
	}

	public void setAu_arrive(boolean au_arrive) {
		this.au_arrive = au_arrive;
	}

	public boolean isAu_read() {
		return au_read;
	}

	public void setAu_read(boolean au_read) {
		this.au_read = au_read;
	}

	public boolean isAu_comment() {
		return au_comment;
	}

	public void setAu_comment(boolean au_comment) {
		this.au_comment = au_comment;
	}

	@Override
	public String toString() {
		return "Au [au_id=" + au_id + ", au_acti=" + au_acti + ", au_user="
				+ au_user + ", au_eval_score=" + au_eval_score
				+ ", au_eval_content=" + au_eval_content + ", au_eval_time="
				+ au_eval_time + ", au_eval_support=" + au_eval_support
				+", au_join=" + au_join + ", au_arrive=" + au_arrive + ", au_read=" + au_read
				+ ", au_comment=" + au_comment + "]";
	}

}
